class Character {
  constructor(name, className, hp, hpMax, mana, manaMax, rage, rageMax, energy, energyMax) {
    this.name = name;
    this.class = className;

    this.hp = hp;
    this.hpMax = hpMax;

    this.mana = mana;
    this.manaMax = manaMax;

    this.rage = rage;
    this.rageMax = rageMax;

    this.energy = energy;
    this.energyMax = energyMax;
  }
}
